// explain the difference between getBy, findBy, queryBy:


// - getBy: برای انتخاب عناصری که حتماً باید وجود داشته باشند. اگر وجود نداشته باشند، خطا ایجاد می‌کند.


// - queryBy: برای انتخاب عناصری که ممکن است وجود نداشته باشند. اگر وجود نداشته باشند، null برمی‌گرداند.


// - findBy: برای انتخاب عناصری که ممکن است به صورت غیرهمزمان (پس از بارگذاری) به DOM اضافه شوند. اگر وجود نداشته باشند، به مدت معین تلاش می‌کند تا آن‌ها را پیدا کند.